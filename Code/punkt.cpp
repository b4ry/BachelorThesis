#include "punkt.h"
#include <math.h>

/***************************************************************************************************************************************/
/* konstruktor parametryczny punktu dla obiektow 2D */
Punkt::Punkt(int x, int y) {
	this->x = x;
	this->y = y;
}
/***************************************************************************************************************************************/
/* zwraca wsp x */
int Punkt::pobierzX() {
	return x;
}
/***************************************************************************************************************************************/
/* zwraca wsp y */
int Punkt::pobierzY() {
	return y;
}
/***************************************************************************************************************************************/
/* zwraca wsp z */
int Punkt::pobierzZ() {
	return z;
}
/***************************************************************************************************************************************/
/* ustawia wsp x na podana wartosc */
void Punkt::ustawX(int x) {
	this->x = x; 
}
/***************************************************************************************************************************************/
/* ustawia wsp y na podana wartosc */
void Punkt::ustawY(int y) {
	this->y = y;
}
/***************************************************************************************************************************************/
/* ustawia wsp z na podana wartosc */
void Punkt::ustawZ(int z) {
	this->z = z;
}
/***************************************************************************************************************************************/
/* obliczanie odleglosci miedzy punktami na plaszczyznie */
int Punkt::odlegoscPunktow2D(Punkt p) {
	double odl_x;
	double odl_y;

	odl_x = this->x - p.pobierzX();
	odl_y = this->y - p.pobierzY();

	return sqrt(odl_x * odl_x - odl_y * odl_y);
}
/***************************************************************************************************************************************/
/* przeciazenie operatora << do wyswietlania wsp punktu */
std::ostream & operator<< (std::ostream &wyjscie, Punkt &p) {
	return wyjscie << "(" << p.pobierzX() << ", " << p.pobierzY() << ")" << std::endl;
}
